#!/usr/bin/env node
// Build khan-video-index.js from a clone of tamnd/khanacademy-index.
// One-shot data import: reads markdown frontmatter for every video file,
// keeps math-related ones, normalizes the title, dedupes, and writes a
// JS module the extension can consume as a fallback corpus when the
// curated KHAN_TOPIC_MAP misses.
//
// Source repo: https://github.com/tamnd/khanacademy-index (CC-BY-SA-4.0)
//
// Usage:
//   node scripts/build-khan-index.js <path-to-khanacademy-index-checkout>

const fs = require('fs');
const path = require('path');

const root = process.argv[2];
if (!root) {
  console.error('Usage: node scripts/build-khan-index.js <khanacademy-index-checkout>');
  process.exit(2);
}

const videosRoot = path.join(root, 'videos');
if (!fs.existsSync(videosRoot)) {
  console.error('Expected videos/ inside', root);
  process.exit(2);
}

// Math-flavored category tokens. We test against the slice of the title
// between the first and last pipe — i.e. the course/series name.
const MATH_PATTERNS = [
  /algebra/i, /geometry/i, /trig/i, /trigonometry/i,
  /calculus/i, /pre[-\s]?calc/i, /precalc/i,
  /statistics/i, /probability/i, /stats/i,
  /arithmetic/i, /pre[-\s]?algebra/i, /prealgebra/i,
  /\bmath/i, /^math\b/i,
  /linear algebra/i, /differential equations/i,
];

function isMathTitle(rawTitle) {
  // Title format: "Topic | Course | Khan Academy". The middle pipe-section
  // names the course; if any math token appears in it, keep the video.
  const parts = rawTitle.split('|').map((s) => s.trim());
  // Even single-part titles count if they contain math tokens.
  const blob = parts.length >= 2 ? parts.slice(1).join(' | ') : parts.join(' ');
  return MATH_PATTERNS.some((re) => re.test(blob));
}

function cleanTitle(rawTitle) {
  // Strip a trailing "| Khan Academy" and the second-to-last "| Course"
  // segment so what's left is the lesson topic.
  const parts = rawTitle.split('|').map((s) => s.trim());
  if (parts.length >= 3 && /khan academy/i.test(parts[parts.length - 1])) {
    return parts.slice(0, -2).join(' | ').trim() || parts[0];
  }
  if (parts.length >= 2 && /khan academy/i.test(parts[parts.length - 1])) {
    return parts.slice(0, -1).join(' | ').trim() || parts[0];
  }
  return rawTitle.trim();
}

function parseFrontmatter(text) {
  // Minimal YAML-ish parser — only handles top-level scalar fields with
  // optional double-quoted strings. Sufficient for this dataset.
  const m = text.match(/^---\n([\s\S]*?)\n---/);
  if (!m) return null;
  const body = m[1];
  const out = {};
  for (const line of body.split('\n')) {
    const kv = line.match(/^([a-zA-Z_]+):\s*(.*)$/);
    if (!kv) continue;
    let v = kv[2].trim();
    if (v.startsWith('"') && v.endsWith('"')) v = v.slice(1, -1);
    out[kv[1]] = v;
  }
  return out;
}

function walk(dir, out) {
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, ent.name);
    if (ent.isDirectory()) walk(p, out);
    else if (ent.isFile() && p.endsWith('.md')) out.push(p);
  }
}

const files = [];
walk(videosRoot, files);

let total = 0, mathy = 0, badId = 0;
const seenIds = new Set();
const entries = [];
for (const file of files) {
  total++;
  const text = fs.readFileSync(file, 'utf8');
  const fm = parseFrontmatter(text);
  if (!fm) continue;
  const id = (fm.video_id || '').trim();
  const title = (fm.title || '').trim();
  if (!/^[A-Za-z0-9_-]{11}$/.test(id)) { badId++; continue; }
  if (!title) continue;
  if (!isMathTitle(title)) continue;
  if (seenIds.has(id)) continue;
  seenIds.add(id);
  mathy++;
  entries.push({ id, title: cleanTitle(title), raw: title });
}

// Sort alphabetically by clean title for stable diffs.
entries.sort((a, b) => a.title.localeCompare(b.title));

const out = {
  generatedAt: new Date().toISOString(),
  source: 'tamnd/khanacademy-index (CC-BY-SA-4.0)',
  totalScanned: total,
  badIds: badId,
  mathCount: mathy,
  entries,
};

const jsonPath = path.join(__dirname, '..', 'khan-video-index.json');
fs.writeFileSync(jsonPath, JSON.stringify(out, null, 2));
console.log(`Wrote ${jsonPath} (${entries.length} math videos out of ${total} scanned).`);

// Also emit a JS module form so content scripts can include it without
// fetching at runtime. Strips metadata, ships only the entries array.
const jsBody = `// Generated by scripts/build-khan-index.js — do not edit by hand.
// Source: tamnd/khanacademy-index (CC-BY-SA-4.0).
// ${entries.length} math-flavored Khan Academy videos. Used as a tier-2
// match source after KHAN_TOPIC_MAP misses.
const KHAN_VIDEO_INDEX = ${JSON.stringify(entries)};
if (typeof window !== 'undefined') window.KHAN_VIDEO_INDEX = KHAN_VIDEO_INDEX;
if (typeof module !== 'undefined') module.exports = { KHAN_VIDEO_INDEX };
`;
const jsPath = path.join(__dirname, '..', 'khan-video-index.js');
fs.writeFileSync(jsPath, jsBody);
console.log(`Wrote ${jsPath} (${(jsBody.length / 1024).toFixed(1)} KB).`);
